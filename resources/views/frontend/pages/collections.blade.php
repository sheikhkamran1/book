        <p>Audio visual materials are one of the important parts of its collection. There are some audio cassettes and video tapes deposited as copyright and some number of encyclopaedias and other databases in CD-ROM. JICA has donated some Japanese animation video tapes to add into the Children’s Section of the library. Educational videos are useful especially for children. Such materials can be used within the premises of library.</p>

        <h3>REPORTS & THESIS:</h3>
        <p>The library has some governmental and other reports in closed access. These can be used upon request.</p><br>

        <h3>DISSERTATIONS/THESES:</h3>
        <p>This library has more than 1200 theses and dissertations of Ph.D. and Master Degree. These theses and dissertations are received from Tribhuvan University Central Library, Kathmandu University and other institutions. The theses and dissertations submitted to the Ministry of Education and Sports for Mahendra Vidya Bhusan are also collected here. These dissertations and theses are in closed access and are available upon request.</p>

        <h3>ADB DEPOSITORY:</h3>
        <p>NNL has been designated as the “Depository Library” for the publications of ADB since August 2001. The collection is available upon request.</p><hr>

        <h3 style="border-bottom:3px solid #a04403;width: fit-content;">Periodicals</h3>
        <p>Approximately 40 newspapers and magazines are currently subscribed, and 25 others are being received as gift.</p><br>
        <p>There are back issues, including many bound volumes, of the major Nepali newspapers, mostly dating from around 1980 (2037 BS), and of Nepali magazines and journals spanning the past 4 decades. A few Nepali periodicals date back to the 1940’s. Many are literary magazines such as Madhu Parka, Juhi, and Damuna, but other holdings of historic interest include early issues of the Nepali Law Review, Sipahi, the journal of the Royal Nepal Army, Ramjham, a current affairs magazine, and Prashashan, journal of Nepali administration.</p><br>
        <p>The section also has many journals from Hem Raj Pandey’s collection, dating from the late nineteenth and early twentieth century. The majority were published in India. English titles include the Calcutta Review, the Journal of the Royal Asiatic Society, the Modern Review, the Theosophist, the Vedic, and many others. There are also many Hindi and Urdu journals from the same period, including some long runs. These holdings are a goldmine of largely unexploited material, of special interest to students and researchers of studies relating to the Indian Subcontinent.</p><br>
        <p>The Periodicals Section has suffered greatly from under-funding over the years, in spite of the fact that the NNL spends more than 50% of its book fund on periodicals. Without adequate budget to maintain subscriptions, the majority of the holdings are now discontinued titles, and it has only been possible to continue binding a few key Nepali newspapers and journals. The very many new Nepali periodical titles which have appeared in the last decade are hardly represented at all in the NNL’s holdings, and many back issues and short-lived titles may have already been lost to the nation.</p>

