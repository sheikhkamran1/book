        <h4>NEPALI COLLECTIONS</h4>
        <p>The Nepali book collection has gradually been built up over the past 40 years, since the establishment of the Nepal National Library. Almost 50% of the collection comprises Nepali literary works, including poetry, drama, essays, short stories, and novels. Of the other subjects covered, the social sciences are most strongly represented, followed by history, biography, religion and language. The collection, which is on open access, is classified by the Dewey Decimal Classification, and author, title and subject indexing is provided by the card catalogue and there is also OPAC facility.</p><hr>

        <h3>SANSKRIT COLLECTIONS</h3>
        <p>The Sanskrit collection contains some rare and valuable books, including printed “lipi” or leaf books. Some items date back to the early nineteenth century. For Sanskrit scholars and researchers this is the most significant collection and one of the largest collections of printed Sanskrit books in Nepal. The collection includes many works of philosophy, religion, history, Ayurveda and Sanskrit literature. The collection can be searched through OPAC.</p><hr>

        <h3 style="border-bottom:3px solid #a04403;width: fit-content;">Microfilms</h3>
        <p>
            One of the major duties of national library is to preserve the national heritage for future generation. Converting the printed text into microforms is one of the methods of preserving the rare items. This library has microfilming equipment and it has microfilmed some newspapers and other rare and valuable materials. The microfilms can be made available to the users upon request for viewing and printing.</p><hr>

